# method doesnt have access to outer variables unless we pass them through invokation. 
# We must edit method to take 3 arguments or define 'limit' insisde method.



# first way
limit = 15

def fib(first_num, second_num, limit)
  while first_num + second_num < limit
    sum = first_num + second_num
    first_num = second_num
    second_num = sum
  end
  sum
end

result = fib(0, 1, limit)
puts "result is #{result}"



